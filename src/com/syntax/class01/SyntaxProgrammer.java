package com.syntax.class01;

public class SyntaxProgrammer {

	public static void main(String[] args) {
		
		//primitive datetypes
		//numeric
		//type name = assign value
		byte a1=127; //-128 to 127
	    short a2=-100; //-32768 to 32767
	    int a3=1000;
	    long a4=10000000000l;
	    
	    // to store floating numbers
	    float a5=1.99f;
	    double a6=9.99999;
	    
	    
	    //represent yes or no (true or false)
	    boolean a7=true;
	    boolean a8=false;
	
	    // to represent single character
	    char a9='C';
	    char a10='R';
	    char a11='7';
	    
	    
	    //int a7 = Error saying duplicate variable
	    
	    
	    
	    
	    System.out.println(a1);
	    System.out.println(a2);
	    System.out.println(a3);
	    System.out.println(a4);
	    System.out.println(a5);
	    System.out.println(a6);
	    System.out.println(a7);
	    System.out.println(a8);
	    System.out.println(a9);
	    System.out.println(a10);
	    System.out.println(a11);
	}
        // code executes top to bottom!!!!
}

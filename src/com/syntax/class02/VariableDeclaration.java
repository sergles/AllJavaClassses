package com.syntax.class02;

public class VariableDeclaration {

	public static void main(String[] args) {
		

		//create variable = assigning value
		//declare a variable = initializing
	
		double $num=12.99;
 		
		//second way to declare a variable
		   //declare a variable
		   //initialize or assign value
		
		int myBox;
		myBox=135;
		myBox=155;
		System.out.println(myBox);
		
		//third way to declare a variable
		
		int num1, num2, num3;//if variable same type, we can declare all in one line
		num1=10;
		num2=20;
		num3=30;
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		
		// what will be output?
		char char1='C';
		char char2='d';
		
		char char3=char1;
		System.out.println(char3);
		
		
		
	}

}
